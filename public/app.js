function getLanguage() {
  return localStorage.getItem("site-language") || "zh";
}

function applyLanguage(language) {
  const strings = dictionary[language] || dictionary.zh;
  document.documentElement.lang = language === "en" ? "en" : "zh-CN";
  document.querySelectorAll("[data-i18n]").forEach((node) => {
    const value = strings[node.dataset.i18n];
    if (value) {
      node.textContent = value;
    }
  });
  document.querySelectorAll("[data-lang-toggle]").forEach((button) => {
    button.textContent = language === "en" ? "中文" : "English";
  });
}

function initShell() {
  document.querySelectorAll("header.site-header:empty").forEach((header) => {
    header.innerHTML = headerTemplate();
  });
  document.querySelectorAll("footer.site-footer:empty").forEach((footer) => {
    footer.innerHTML = footerTemplate();
  });
}

function initNavigation() {
  const toggle = document.querySelector(".nav-toggle");
  const nav = document.querySelector(".site-nav");
  if (!toggle || !nav) return;

  const currentPath = window.location.pathname === "/index.html" ? "/" : window.location.pathname;
  nav.querySelectorAll("a[href]").forEach((link) => {
    const linkPath = new URL(link.getAttribute("href"), window.location.origin).pathname;
    const isActive = currentPath === linkPath || (currentPath === "/" && linkPath === "/");
    link.classList.toggle("active", isActive);
    if (isActive) {
      link.setAttribute("aria-current", "page");
    } else {
      link.removeAttribute("aria-current");
    }
  });

  toggle.addEventListener("click", () => {
    const isOpen = nav.classList.toggle("open");
    toggle.setAttribute("aria-expanded", String(isOpen));
  });
}

function initLanguageToggle() {
  document.querySelectorAll("[data-lang-toggle]").forEach((button) => {
    button.addEventListener("click", () => {
      const nextLanguage = getLanguage() === "en" ? "zh" : "en";
      localStorage.setItem("site-language", nextLanguage);
      applyLanguage(nextLanguage);
    });
  });
}

function initContactForm() {
  const form = document.querySelector("[data-contact-form]");
  if (!form) return;

  const status = document.querySelector("[data-form-status]");
  form.addEventListener("submit", async (event) => {
    event.preventDefault();
    const language = getLanguage();
    const strings = dictionary[language] || dictionary.zh;
    status.textContent = strings["form.sending"];
    status.className = "form-status";

    const payload = Object.fromEntries(new FormData(form).entries());
    payload.language = language;

    try {
      const response = await fetch("/api/contact", {
        method: "POST",
        headers: { "Content-Type": "application/json" },
        body: JSON.stringify(payload)
      });
      const result = await response.json();
      status.textContent = result.message || strings["form.error"];
      status.classList.toggle("success", response.ok);
      status.classList.toggle("error", !response.ok);
      if (response.ok) form.reset();
    } catch (error) {
      status.textContent = strings["form.error"];
      status.classList.add("error");
    }
  });
}

